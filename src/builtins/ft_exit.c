#include "sh21.h"

int		ft_exit(t_gen *gen, char **tab)
{
	(void)gen;
	if (tab[1] && (ft_isdigit(tab[1][0]) || (tab[1][0] == '-'
					&& ft_isdigit(tab[1][1]))))
		exit(ft_atoi(tab[1]));
	else
		exit(0);
}
