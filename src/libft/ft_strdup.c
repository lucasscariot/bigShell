#include "libft.h"

char	*ft_strdup(char *str)
{
	char	*dup;
	int		i;

	i = 0;
	if (str == NULL)
		return (NULL);
	dup = (char *)malloc(sizeof(char) * (ft_strlen(str) + 1));
	while (str[i] != '\0')
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
