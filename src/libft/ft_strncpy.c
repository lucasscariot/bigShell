#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		end;

	i = 0;
	end = 0;
	while (i < n)
	{
		if (s2[i] && !end)
			s1[i] = s2[i];
		else
		{
			s1[i] = '\0';
			end = 1;
		}
		i++;
	}
	return (s1);
}
